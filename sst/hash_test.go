func TestParse(t *testing.T) {
	type args struct {
		str string
	}
	tests := []struct {
		name      string
		args      args
		wantH     Hash
		assertion assert.ErrorAssertionFunc
	}{
		{
			name: "nil_hash",
			args: args{
				str: HashNil().String(),
			},
			wantH:     HashNil(),
			assertion: assert.NoError,
		},
		{
			name: "parse_given_hash",
			args: args{
				str: "8K9tBpgidXxH6cwN4QvHNfjTNogrgjfg7b5KgztdueHF",
			},
			wantH: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0x98, 0x95, 0x60, 0x31, 0xd5, 0xde, 0x58, 0x94,
				0x93, 0x5c, 0xc8, 0xe1, 0x2b, 0x9b, 0x46, 0x69, 0x8e, 0xb3, 0x98, 0xa2, 0x06, 0x45, 0x5e, 0xe2,
			},
			assertion: assert.NoError,
		},
		{
			name: "parse_given_shortened_lower_hash",
			args: args{
				str: "8K9tBpgidXxH6cwN4QvHNfjTNogrgjfg7b5Kgztd1111",
			},
			wantH: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0x98, 0x95, 0x60, 0x31, 0xd5, 0xde, 0x58, 0x94,
				0x93, 0x5c, 0xc8, 0xe1, 0x2b, 0x9b, 0x46, 0x69, 0x8e, 0xb3, 0x98, 0xa2, 0x05, 0xa8, 0xa4, 0xe0,
			},
			assertion: assert.NoError,
		},
		{
			name: "parse_given_shortened_upper_hash",
			args: args{
				str: "8K9tBpgidXxH6cwN4QvHNfjTNogrgjfg7b5Kgztdzzzz",
			},
			wantH: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0x98, 0x95, 0x60, 0x31, 0xd5, 0xde, 0x58, 0x94,
				0x93, 0x5c, 0xc8, 0xe1, 0x2b, 0x9b, 0x46, 0x69, 0x8e, 0xb3, 0x98, 0xa2, 0x06, 0x55, 0x51, 0xef,
			},
			assertion: assert.NoError,
		},
		{
			name: "parse_with_zeros_in_front",
			args: args{
				str: "1111111111111111111111111111111111111111zzzz",
			},
			wantH: Hash{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xac, 0xad, 0x0f,
			},
			assertion: assert.NoError,
		},
		{
			name: "parse_all_zeros",
			args: args{
				str: "11111111111111111111111111111111111111111111",
			},
			wantH: Hash{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			},
			assertion: assert.NoError,
		},
		{
			name: "parse_truncated_hash",
			args: args{
				str: "1iYk6EXrc3ubkVskCTq6wW2S2yManwcXqtBPkBBNVZPT",
			},
			wantH: Hash{
				0x0a, 0xa4, 0xb4, 0x7c, 0x64, 0xc5, 0x3b, 0xe4, 0x13, 0xb8, 0xc5, 0x55, 0x58, 0xa0, 0x2f, 0xbb,
				0x04, 0xec, 0x9c, 0xb5, 0x00, 0x2d, 0xf6, 0x89, 0x99, 0xbf, 0x74, 0x08, 0xb3, 0x7c, 0xf8, 0x86,
			},
			assertion: assert.NoError,
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			gotH, err := StringToHash(tt.args.str)
			tt.assertion(t, err)
			assert.Equal(t, tt.wantH, gotH)
		})
	}
}

func TestParseShort(t *testing.T) {
	type args struct {
		str string
	}
	tests := []struct {
		name        string
		args        args
		wantMinHash Hash
		wantMaxHash Hash
		assertion   assert.ErrorAssertionFunc
	}{
		{
			name: "hash_8K_td",
			args: args{
				str: "8K9tBpgidXxH6cwN4QvHNfjTNogrgjfg7b5Kgztd",
			},
			wantMinHash: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0x98, 0x95, 0x60, 0x31, 0xd5, 0xde, 0x58, 0x94,
				0x93, 0x5c, 0xc8, 0xe1, 0x2b, 0x9b, 0x46, 0x69, 0x8e, 0xb3, 0x98, 0xa2, 0x05, 0xa8, 0xa4, 0xe0,
			},
			wantMaxHash: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0x98, 0x95, 0x60, 0x31, 0xd5, 0xde, 0x58, 0x94,
				0x93, 0x5c, 0xc8, 0xe1, 0x2b, 0x9b, 0x46, 0x69, 0x8e, 0xb3, 0x98, 0xa2, 0x06, 0x55, 0x51, 0xef,
			},
			assertion: assert.NoError,
		},
		{
			name: "hash_8K_xH",
			args: args{
				str: "8K9tBpgidXxH",
			},
			wantMinHash: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0x97, 0x85, 0x4c, 0x08, 0xc3, 0xb6, 0x0c, 0x98,
				0x73, 0xf6, 0xfc, 0x6c, 0xf5, 0x73, 0xf4, 0xce, 0xd3, 0xb3, 0xdc, 0x0e, 0x00, 0x00, 0x00, 0x00,
			},
			wantMaxHash: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0xa2, 0x7d, 0x6c, 0x3c, 0x21, 0x51, 0x4a, 0x35,
				0x69, 0x82, 0x8d, 0x8a, 0x7c, 0x77, 0x4b, 0x46, 0xcf, 0x33, 0x2e, 0x8e, 0xff, 0xff, 0xff, 0xff,
			},
			assertion: assert.NoError,
		},
		{
			name: "hash_8K9tBp",
			args: args{
				str: "8K9tBp",
			},
			wantMinHash: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x08, 0xb7, 0xcc, 0x4d, 0xfd, 0x52, 0x57, 0x62, 0x88, 0x0b, 0xea, 0x23,
				0xba, 0x29, 0xed, 0x05, 0xd6, 0x83, 0xb2, 0xbf, 0xed, 0xc1, 0x91, 0xc0, 0x00, 0x00, 0x00, 0x00,
			},
			wantMaxHash: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x69, 0xf1, 0xc8, 0xcb, 0x18, 0x67, 0x8a, 0x21, 0x7b, 0x5f, 0xe6, 0xd6,
				0xbe, 0x54, 0xaa, 0x13, 0x9a, 0xb6, 0x4d, 0x48, 0xdd, 0xe7, 0x57, 0xff, 0xff, 0xff, 0xff, 0xff,
			},
			assertion: assert.NoError,
		},
		{
			name: "hash_JEKNVn",
			args: args{
				str: "JEKNVn",
			},
			wantMinHash: Hash{
				0xff, 0xff, 0xff, 0xff, 0xb6, 0xe9, 0xa4, 0x3a, 0xe9, 0x61, 0x8f, 0x66, 0x42, 0x53, 0x5b, 0x9c,
				0x43, 0x93, 0x1e, 0x3d, 0xbe, 0x5b, 0x9c, 0x5c, 0x5c, 0x8a, 0xc4, 0x40, 0x00, 0x00, 0x00, 0x00,
			},
			wantMaxHash: Hash{
				0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
				0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff, 0xff,
			},
			assertion: assert.NoError,
		},
		{
			name: "hash_JEkNVn_overflows_with_illegal_hash_length",
			args: args{
				str: "JEkNVn",
			},
			wantMinHash: Hash{},
			wantMaxHash: Hash{},
			assertion: func(t assert.TestingT, err error, _ ...interface{}) bool {
				return assert.ErrorIs(t, err, ErrIllegalHashLength)
			},
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			gotMinHash, gotMaxHash, err := hashBase58ParseShort(tt.args.str)
			tt.assertion(t, err)
			assert.Equal(t, tt.wantMinHash, gotMinHash)
			assert.Equal(t, tt.wantMaxHash, gotMaxHash)
		})
	}
}

func TestHash_String(t *testing.T) {
	tests := []struct {
		name string
		i    Hash
		want string
	}{
		{
			name: "hash_6c_e2",
			i: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0x98, 0x95, 0x60, 0x31, 0xd5, 0xde, 0x58, 0x94,
				0x93, 0x5c, 0xc8, 0xe1, 0x2b, 0x9b, 0x46, 0x69, 0x8e, 0xb3, 0x98, 0xa2, 0x06, 0x45, 0x5e, 0xe2,
			},
			want: "8K9tBpgidXxH6cwN4QvHNfjTNogrgjfg7b5KgztdueHF",
		},
		{
			name: "hash_0a_86",
			i: Hash{
				0x0a, 0xa4, 0xb4, 0x7c, 0x64, 0xc5, 0x3b, 0xe4, 0x13, 0xb8, 0xc5, 0x55, 0x58, 0xa0, 0x2f, 0xbb,
				0x04, 0xec, 0x9c, 0xb5, 0x00, 0x2d, 0xf6, 0x89, 0x99, 0xbf, 0x74, 0x08, 0xb3, 0x7c, 0xf8, 0x86,
			},
			want: "1iYk6EXrc3ubkVskCTq6wW2S2yManwcXqtBPkBBNVZPT",
		},
		{
			name: "hash_6c_d4",
			i: Hash{
				0x6c, 0xa8, 0x4e, 0x7a, 0x4b, 0x4c, 0x2f, 0x43, 0x98, 0x95, 0x60, 0x31, 0xd5, 0xde, 0x58, 0x94,
				0x93, 0x5c, 0xc8, 0xe1, 0x2b, 0x9b, 0x46, 0x69, 0x8e, 0xb3, 0x98, 0xa2, 0x06, 0x45, 0x5e, 0xd4,
			},
			want: "8K9tBpgidXxH6cwN4QvHNfjTNogrgjfg7b5KgztdueH1",
		},
		{
			name: "hash_00_0f",
			i: Hash{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0xac, 0xad, 0x0f,
			},
			want: "1111111111111111111111111111111111111111zzzz",
		},
		{
			name: "hash_all_00",
			i: Hash{
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
				0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
			},
			want: "11111111111111111111111111111111111111111111",
		},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			assert.Equal(t, tt.want, tt.i.String())
		})
	}
}
